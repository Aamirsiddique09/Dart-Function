// Dart Functions Example

// Function to greet with a given name
void greet(String name) {
  print('Hello, $name!');
}

// Function to calculate the sum of two numbers
int sum(int a, int b) {
  return a + b;
}

void main() {
  // Greeting
  greet('World');

  // Sum of two numbers
  int result = sum(3, 5);
  print('Sum: $result');
}
